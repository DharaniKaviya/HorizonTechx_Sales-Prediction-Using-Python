"""
app.py
======
Flask application for Sales Prediction Using Python.

Routes
------
GET  /            → index.html   (Landing / Hero page)
GET  /dashboard   → dashboard.html (Analytics + Model Comparison)
GET  /analysis    → analysis.html  (EDA charts gallery)
GET  /predict     → predict.html   (Prediction form)
POST /result      → result.html    (Prediction result)

Run:
    set FLASK_APP=app.py
    flask run --debug
  OR
    python app.py
"""

import os
import json

from flask import Flask, render_template, request, redirect, url_for, flash

app = Flask(__name__)
app.secret_key = "sales_prediction_secret_key_2024"

BASE_DIR  = os.path.dirname(os.path.abspath(__file__))
META_PATH = os.path.join(BASE_DIR, "model_meta.json")
IMG_DIR   = os.path.join(BASE_DIR, "static", "images")

# ---------------------------------------------------------------------------
# Helper: load model metadata (generated by train_model.py)
# ---------------------------------------------------------------------------
def load_meta() -> dict:
    if os.path.exists(META_PATH):
        with open(META_PATH, "r", encoding="utf-8") as f:
            return json.load(f)
    # Fallback defaults if model not yet trained
    return {
        "total_records":    1200,
        "num_features":     10,
        "avg_sales":        850.0,
        "max_sales":        3200.0,
        "min_sales":        50.0,
        "best_model":       "Not trained yet",
        "best_r2":          0.0,
        "model_accuracy":   0.0,
        "model_results":    {},
        "best_platform":    "N/A",
        "best_channel":     "N/A",
        "best_region":      "N/A",
        "best_month":       "N/A",
        "predictions_made": 0,
    }


def get_eda_images() -> list:
    """Return list of EDA image filenames that exist in static/images/."""
    wanted = [
        ("sales_distribution.png",  "Sales Distribution"),
        ("spend_distribution.png",  "Advertising Spend Distribution"),
        ("platform_sales.png",      "Platform-wise Sales"),
        ("campaign_sales.png",      "Campaign-wise Sales"),
        ("region_sales.png",        "Region-wise Sales Share"),
        ("monthly_trend.png",       "Monthly Sales Trend"),
        ("correlation_heatmap.png", "Correlation Heatmap"),
        ("scatter_plots.png",       "Spend vs Sales Scatter"),
        ("boxplot_platform.png",    "Boxplot by Platform"),
        ("feature_importance.png",  "Feature Importance"),
        ("model_comparison.png",    "Model Comparison"),
    ]
    result = []
    for fname, label in wanted:
        if os.path.exists(os.path.join(IMG_DIR, fname)):
            result.append({"file": fname, "label": label})
    return result


# ---------------------------------------------------------------------------
# Routes
# ---------------------------------------------------------------------------

@app.route("/")
def index():
    meta = load_meta()
    return render_template("index.html", meta=meta)


@app.route("/dashboard")
def dashboard():
    meta   = load_meta()
    images = get_eda_images()
    return render_template("dashboard.html", meta=meta, images=images)


@app.route("/analysis")
def analysis():
    meta   = load_meta()
    images = get_eda_images()
    return render_template("analysis.html", meta=meta, images=images)


@app.route("/predict")
def predict():
    meta = load_meta()
    platforms      = ["Google Ads", "Facebook", "Instagram", "YouTube", "Twitter", "LinkedIn"]
    campaign_types = ["Conversion", "Lead Generation", "Brand Awareness", "Retargeting", "Seasonal Offer"]
    regions        = ["North", "South", "East", "West", "Central"]
    months         = ["January","February","March","April","May","June",
                      "July","August","September","October","November","December"]
    audiences      = ["18-24", "25-34", "35-44", "45-54", "55+"]
    return render_template("predict.html", meta=meta,
                           platforms=platforms,
                           campaign_types=campaign_types,
                           regions=regions,
                           months=months,
                           audiences=audiences)


@app.route("/result", methods=["POST"])
def result():
    # Check if model is trained
    model_pkl = os.path.join(BASE_DIR, "model.pkl")
    if not os.path.exists(model_pkl):
        flash("⚠️  Model not trained yet. Please run train_model.py first.", "error")
        return redirect(url_for("predict"))

    try:
        from predict import predict_sales

        input_data = {
            "TV_Spend":           request.form.get("tv_spend", 0),
            "Radio_Spend":        request.form.get("radio_spend", 0),
            "Newspaper_Spend":    request.form.get("newspaper_spend", 0),
            "Social_Media_Spend": request.form.get("social_spend", 0),
            "Digital_Spend":      request.form.get("digital_spend", 0),
            "Platform":           request.form.get("platform", "Google Ads"),
            "Campaign_Type":      request.form.get("campaign_type", "Conversion"),
            "Region":             request.form.get("region", "North"),
            "Month":              request.form.get("month", "January"),
            "Target_Audience":    request.form.get("audience", "25-34"),
        }

        prediction = predict_sales(input_data)
        meta = load_meta()

        # Increment prediction counter in meta
        meta["predictions_made"] = meta.get("predictions_made", 0) + 1
        with open(META_PATH, "w", encoding="utf-8") as f:
            json.dump(meta, f, indent=2)

        return render_template("result.html",
                               meta=meta,
                               prediction=prediction,
                               input_data=input_data)

    except Exception as exc:
        flash(f"Prediction error: {str(exc)}", "error")
        return redirect(url_for("predict"))


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------
if __name__ == "__main__":
    app.run(debug=True, host="0.0.0.0", port=5000)
